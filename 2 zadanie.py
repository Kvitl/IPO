# Ввод данных
set1 = set(map(int, input("Введите элементы первого множества через пробел: ").split()))
set2 = set(map(int, input("Введите элементы второго множества через пробел: ").split()))

# Нахождение всех различных чисел в обоих множествах
distinct_numbers = set1.union(set2)

# Нахождение чисел, которые входят и в первое, и во второе множества
common_numbers = sorted(set1.intersection(set2))

# Вывод результатов
print("Все различные числа в обоих множествах:", distinct_numbers)
print("Числа, которые входят и в первое, и во второе множества (в порядке возрастания):", common_numbers)